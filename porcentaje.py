# Escribe un programa que solicite al usuario
# un número y le reste el 15%, almacenando todo en una única variable
# A continuación, mostrar el resultado final en pantalla

Numero = float(input("Ingrese el numero que quiere restar: "))

Resultado = Numero * 15/100

Resta = Numero - Resultado

print("El resultado del número restado por el 15% : "+ str(Resta))

